# Tipos de Datos en Python

# Integers
numeroEntero = 33
print(numeroEntero)
print(type(numeroEntero))
print("---------------------------------")


# Float (decimal)
numeroFlotante = 3.1415
print(numeroFlotante)
print(type(numeroFlotante))
print("---------------------------------")


# Booleans (bool)
print(True)
print(False)
print(type(True))
print("---------------------------------")


# Strings
letra = 'a'
palabra = "holaaaa"
print(letra)
print(type(letra))
print(palabra)
print("---------------------------------")


# NoneType
print(None)
print(type(None))